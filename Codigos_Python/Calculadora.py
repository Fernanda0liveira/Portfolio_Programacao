operador = input()
numero = int(input())
numero2 = int(input())
if (operador == "+"):
  print(numero+numero2)
elif (operador == "-"):
  print(numero-numero2)
elif(operador == "*"):
  print(numero*numero2)
elif(operador == "/"):
  print(numero/numero2)
elif(operador == "//"):
  print(numero//numero2)
elif(operador == "%"):
  print(numero%numero2)
elif(operador == "**"):
  print(numero**numero2)
